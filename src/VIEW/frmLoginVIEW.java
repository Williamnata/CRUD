    private void btnentrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnentrarActionPerformed
        try {
            String nome, senha;

            nome = txtusuario.getText();
            senha = txtsenha.getText();

            UsuarioDTO objusuariodto = new UsuarioDTO();
            objusuariodto.setNome_usuario(nome);
            objusuariodto.setSenha_usuario(senha);

            usuarioDAO objusuariodao = new usuarioDAO();
            ResultSet rsusuariodao = objusuariodao.autenticacaoUsuario(objusuariodto);
            if (rsusuariodao.next()) {
                //chamar tela
                frmPrincipalVIEW objfrmprincipalview = new frmPrincipalVIEW();
                objfrmprincipalview.setVisible(true);
                dispose();
            } else {
                // enviar mensagem de erro
                JOptionPane.showInputDialog(null, "Usuario ou senha inv√°lida");
            }
        } catch (SQLException erro) {
            JOptionPane.showInputDialog(null, "FRMLoginView" + erro);
        }
    }//GEN-LAST:event_btnentrarActionPerformed

